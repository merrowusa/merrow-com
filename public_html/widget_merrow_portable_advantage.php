Overlock stitching was invented by the Merrow Machine Company in 1881. Merrow's original three-thread overedge sewing machine is the forerunner of contemporary overlocking machines. Over time, the Merrow Machine Company pioneered the design of new machines to create a variety of overlock stitches, such as two, and four-thread sergers, the one-thread butted seam, and the cutterless emblem edger. Today Two- and three-thread formations are also known as "merrowing". <br /><br />Since 2006 Merrow Machines are engineered and built in Fall River, MA. 
