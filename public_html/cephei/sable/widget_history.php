	<h1>About Merrow</h1>
				
	<h2>Merrow - perhaps the most interesting sewing machine company in the world has <i>two main goals</i>:</h2>
		
	<blockquote>

		
		<h3><b>1.</b> <font color="black">We want to <b>help people understand and produce a great stitch</b>.</font></h3>

		<p>In 1838 Merrow began as a textile company, manufacturing clothing (as well as gun powder). In the early 1900s, having invented overlock stitching, Merrow was among the most common sewing machines used worldwide. In 2008, we have become a champion of the highest quality of sewn goods. </p>
		<p>To do this, we build great sewing machines <b>and support the thousands of different stitches that our machines can produce</b> </p>
		<p>Merrow is the company that understands that it's ok to pay a little more for great quality -- as long as you know what you're paying for. </p>

		
		<h3><b>2.</b>	<font color="black">We want to <b>build great sewing machines</b>.</font> </h3>
				
		<p>You need to design, and then produce your work. We want to be the best company in the world of supporting both processes. Our sewing machines are the connection-- made accessible to the home, sample room and cottage industry; where the design process starts. And made available in quantity to the industrial markets worldwide; where production on scale happens. There are more than 300,000 Merrow Machines used worldwide. We estimate there are Tens of Thousands of variations of our machines working all day every day.  </p>
		<p>The Merrow Machine is unique by design, and its stitch is the best in the world.  </p>
	</blockquote>
		
	<div class="bottom_paragraph"><p>Merrow has evolved from a mill, to the largest manufacturer of overlock machines in the world, to a brand of stitching and the best place on earth to understand, develop and produce new sewn products. 
		<a href="http://merrow.com/cephei/sable/fp_merrow.php"><b>We hope you enjoy it all! </b></a></p>

