	<h1>About Merrow</h1>
				
	<h2>Merrow - perhaps the most interesting sewing machine company in the world has <i>two main goals</i>:</h2>
		
	<blockquote>

		
				
		<h3><b>1.</b>	<font color="black">We <b>build great sewing machines</b>.</font> </h3>
				
		<p>Merrow builds the worlds' best overlock sewing machines and specializes in customizing them for specific applications. Our sewing machines are the connection between the sample room and the factory floor. <br><br>Merrow Machines are designed for the modern industrial sewing room and are available in quantity to the industrial markets worldwide; where production on scale happens. But our Sample Room is designed for the production manager, student or fashion designer. We will build a stitch that works perfectly for a specific application, and then produce the machines needed to build it in quantity. <br><br>This has been a successful formula: there are more than 300,000 Merrow Machines used worldwide. We estimate there are Tens of Thousands of variations of our machines working all day every day.  </p>
		<p>The Merrow Machine is unique by design, and its stitch is the best in the world.  </p>
		
		<h3><b>2.</b> <font color="black">We help people<b> produce a valuable stitch</b>.</font></h3>
		
				<p><b>The Stitch Matters</b> It Means a lot to call a stitch a Merrow. Merrow Sewing Machines incorporate a unique cam driven technology which achieves more consistent, technically superior stitches. The result is that products stitched on Merrow machines have better seams, last longer and wear better.</p><p><b> Stitching matters, we know it, and now so do you.</b> Producing textiles, garments, and other sewn products with a Merrow Stitch creates value in the marketplace, allowing customers to distinguish between a high quality designed stitch, and everything else. </p>
			
				<p>To do this, we build great sewing machines <b>and support the thousands of different stitches that our machines can produce with spare parts milled to .001" tolerances (in many cases). We support just about every machine we've ever made, and still ship parts for machines manufactured in the 1800s!</b> </p>
				
		
		
	</blockquote>
		
	<div class="bottom_paragraph"><p>Merrow has evolved from a mill, to the largest manufacturer of overlock machines in the world, to a brand of stitching and the best place on earth to understand, develop and produce new sewn products. 
		<a href="http://merrow.com/"><b>We hope you enjoy it all! </b></a></p></div>
		
		
		
				
		
